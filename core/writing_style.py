"""Shared prose constraints for chapter-writing agents."""

LEAN_HUMAN_PROSE_GUIDE = """
【正文风格：克制、洗练的原创白描】
1. 使用第三人称限知视角。叙述冷静、简洁、直接，不模仿或复刻任何具体作家的独特文风。
2. 句式必须简单直白。优先使用“谁做了什么”的清楚句式，少用长定语、倒装句、嵌套从句和故作深沉的断裂句。
3. 不要词藻堆砌。准确、自然比华丽重要；同一句话不得叠加多个形容词、比喻、意象或情绪判断。
4. 以人物的选择、动作、对话和后果讲故事。能让人物说出来或做出来的信息，不由旁白解释。
5. 多用短句和短段。一个段落通常只承载一个动作、一个念头或一轮对话；关键句可以单独成段，但不要机械地每句都换段。
6. 对话必须有目的：制造冲突、暴露立场、传递新信息或改变关系。删除寒暄、复述和无效问答。
7. 禁止独立的景物描写段。环境只保留影响行动的必要信息，每次一两笔，不渲染天气、光影、气味和氛围。
8. 禁止外貌清单、服饰陈列和反复描写表情。人物状态优先通过动作、语气、停顿和选择表现。
9. 少用形容词、副词、比喻、排比、感叹和抽象抒情。避免“仿佛、宛如、似乎、不由得、空气中弥漫着、心中涌起”等模板化表达。
10. 保持诙谐，但幽默来自人物处境、认知错位和一本正经的反应，不靠段子堆砌，不拿网络梗代替剧情。
11. 网络流行语每章最多一至两处；只在人物身份和语境自然时使用，避开过期梗、谐音烂梗和强行玩梗。
12. 禁止旁白总结主题、解释人物为什么感动、替读者下结论。写出事实，让读者自己感受。
13. 每写完一个场景，删掉不影响情节、人物或节奏的句子；不得靠环境描写、回忆复述或同义反复凑字数。
""".strip()
